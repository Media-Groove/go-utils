package utils

func StrPtr(s string) *string {
	return &s
}

func BoolPtr(b bool) *bool {
	return &b
}

func IntPtr(n int) *int {
	return &n
}

func Int32Ptr(n int32) *int32 {
	return &n
}

func Int64Ptr(n int64) *int64 {
	return &n
}

func Float32Ptr(n float32) *float32 {
	return &n
}

func Float64Ptr(n float64) *float64 {
	return &n
}
